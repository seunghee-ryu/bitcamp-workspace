//# 할당(배정,대입) 연산자  : +=  -=  *=  /=  %=  &=  |=  ^=  <<=  >>=  >>>=
// assignment operator
public class Exam0710t {
  public static void main(String[] args) {
    int i = 2;
    i += 20; // 22
    System.out.println(i); //22
    
    i = 2;
    i *= 5;
    System.out.println(i); //10

  }
}
