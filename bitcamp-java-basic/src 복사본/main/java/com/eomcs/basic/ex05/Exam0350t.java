package com.eomcs.basic.ex05;

public class Exam0350t {
  public static void main(String[] args) {
    int a = 0b0110_1100; // 108
    int b = 0b0101_0101; // 85
    
    // 정수 값에 대해선 &&와 ||, !을 사용할 수 없다.
    // 그러나 &, |, ^, ~ 는 사용 가능하다.
    // 1은 true 2는 false로 간주한다
    // 출력 결과도 정수이다.
    
    
    System.out.println(a & b);
    // 0000 0000 0000 0000 0000 0000 0110 1100
    // 0000 0000 0000 0000 0000 0000 0101 0101
    // ----------------------------------------
    // 0000 0000 0000 0000 0000 0000 0100 0100
    
    System.out.println(a | b);
    // 0000 0000 0000 0000 0000 0000 0110 1100
    // 0000 0000 0000 0000 0000 0000 0101 0101
    // ----------------------------------------
    // 0000 0000 0000 0000 0000 0000 0111 1101
    
    System.out.println(a ^ b);
    // 0000 0000 0000 0000 0000 0000 0110 1100
    // 0000 0000 0000 0000 0000 0000 0101 0101
    // ----------------------------------------
    // 0000 0000 0000 0000 0000 0000 0011 1001
    
    System.out.println(~a);
    // 0000 0000 0000 0000 0000 0000 0110 1100
    // ----------------------------------------
    // 1111 1111 1111 1111 1111 1111 1001 0011
    
    
    
    
    
    
    
  }

}
